#define MAXN 32+10

int t;
ll x;
ll y=0;
int flag=0,j=0,k=0;
int bin[MAXN],ans[MAXN];

int main(int argc, char const *argv[])
{
    scanf("%d", &t);
    while(t--)
    {
        memset(bin,0,sizeof(bin));
        memset(ans,0,sizeof(ans));
        y=0,flag=0,j=0,k=0;
        scanf("%I64d", &x);
        for(int i=31; i>=0; i--)
        {
            bin[flag++]=x>>i&1;
        }
        for(j=0;j<flag;j++)
        {
            if(bin[j]==1)   break;
        }
        for(int i=flag-1;i>=j;i--)
        {
            ans[k++]=bin[i];
        }
        for(int i=0;i<k;i++)
        {
            y=(y+ans[i])*2;
        }
        printf("%I64d\n", y/2);
    }
    return 0;
}

